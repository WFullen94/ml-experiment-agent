from __future__ import annotations

from agent.core.types import Plan

# Reasonable bounds used by static validation.
_RANK_MAX = 512
_LR_MAX = 1.0
_LR_MIN = 1e-7


class ScaffoldGenerator:
    """
    Produces a runnable training config + code stub for each run in a plan,
    without executing anything. Called when the gate returns SCAFFOLD.
    """

    def __call__(self, plan: Plan) -> dict:
        runs = [_build_run(run, plan) for run in plan.runs]
        issues = _validate_all(runs)

        return {
            "dataset":    plan.dataset,
            "metric":     plan.metric,
            "runs":       runs,
            "validation": {
                "configs_valid": len(issues) == 0,
                "issues":        issues,
            },
            "cost_estimate": (
                f"~{999.0 * len(plan.runs):.0f} GPU-min if executed "
                f"(scaffold only — not run)"
            ),
        }


# --- per-run scaffold --------------------------------------------------------

def _build_run(run, plan: Plan) -> dict:
    method = run.method
    hp = run.hyperparams or {}

    if method in ("lora", "qlora"):
        config = _lora_config(method, hp, plan)
        code = _lora_code(config)
    elif method == "full_ft":
        config = _full_ft_config(hp, plan)
        code = _full_ft_code(config)
    else:
        config = {"method": method, "dataset": plan.dataset, **hp}
        code = _generic_stub(method, config)

    return {"method": method, "config": config, "code": code}


def _lora_config(method: str, hp: dict, plan: Plan) -> dict:
    rank = int(hp.get("rank", 8))
    return {
        "method":         method,
        "base_model":     hp.get("base_model", "distilbert-base-uncased"),
        "dataset":        plan.dataset,
        "rank":           rank,
        "alpha":          int(hp.get("alpha", rank * 2)),
        "dropout":        float(hp.get("dropout", 0.1)),
        "target_modules": hp.get("target_modules", ["q_lin", "v_lin"]),
        "learning_rate":  float(hp.get("learning_rate", 2e-4)),
        "num_epochs":     int(hp.get("num_epochs", 3)),
        "batch_size":     int(hp.get("batch_size", 16)),
        "metric":         plan.metric,
    }


def _full_ft_config(hp: dict, plan: Plan) -> dict:
    return {
        "method":        "full_ft",
        "base_model":    hp.get("base_model", "distilbert-base-uncased"),
        "dataset":       plan.dataset,
        "learning_rate": float(hp.get("learning_rate", 2e-5)),
        "num_epochs":    int(hp.get("num_epochs", 3)),
        "batch_size":    int(hp.get("batch_size", 16)),
        "metric":        plan.metric,
    }


# --- static validation -------------------------------------------------------

def _validate_all(runs: list[dict]) -> list[str]:
    issues = []
    for r in runs:
        issues.extend(_validate_run(r))
    return issues


def _validate_run(run: dict) -> list[str]:
    issues = []
    cfg = run["config"]
    method = run["method"]

    if method in ("lora", "qlora"):
        rank = cfg.get("rank", 0)
        alpha = cfg.get("alpha", 0)
        if not (1 <= rank <= _RANK_MAX):
            issues.append(f"{method}: rank={rank} out of valid range [1, {_RANK_MAX}]")
        if alpha <= 0:
            issues.append(f"{method}: alpha={alpha} must be positive")
        if rank > 0 and alpha < rank:
            issues.append(
                f"{method}: alpha={alpha} < rank={rank} — unusual; "
                f"alpha is typically >= rank"
            )

    lr = cfg.get("learning_rate")
    if lr is not None and not (_LR_MIN <= lr <= _LR_MAX):
        issues.append(
            f"{method}: learning_rate={lr} outside sane range "
            f"[{_LR_MIN}, {_LR_MAX}]"
        )

    epochs = cfg.get("num_epochs")
    if epochs is not None and epochs < 1:
        issues.append(f"{method}: num_epochs={epochs} must be >= 1")

    batch = cfg.get("batch_size")
    if batch is not None and batch < 1:
        issues.append(f"{method}: batch_size={batch} must be >= 1")

    return issues


# --- code templates ----------------------------------------------------------

def _lora_code(cfg: dict) -> str:
    return f"""\
# LoRA fine-tuning scaffold — generated by ml-experiment-agent
# Install: pip install transformers peft datasets evaluate
from datasets import load_dataset
from peft import LoraConfig, get_peft_model, TaskType
from transformers import AutoModelForSequenceClassification, AutoTokenizer, TrainingArguments, Trainer

BASE_MODEL = "{cfg['base_model']}"
DATASET    = "{cfg['dataset']}"
RANK       = {cfg['rank']}
ALPHA      = {cfg['alpha']}
LR         = {cfg['learning_rate']}
EPOCHS     = {cfg['num_epochs']}
BATCH_SIZE = {cfg['batch_size']}

dataset   = load_dataset("glue", DATASET)
tokenizer = AutoTokenizer.from_pretrained(BASE_MODEL)
model     = AutoModelForSequenceClassification.from_pretrained(BASE_MODEL, num_labels=2)

lora_cfg = LoraConfig(
    task_type=TaskType.SEQ_CLS,
    r=RANK,
    lora_alpha=ALPHA,
    lora_dropout={cfg['dropout']},
    target_modules={cfg['target_modules']},
)
model = get_peft_model(model, lora_cfg)
model.print_trainable_parameters()

def tokenize(batch):
    return tokenizer(batch["sentence"], truncation=True, padding="max_length")

tokenized = dataset.map(tokenize, batched=True)

args = TrainingArguments(
    output_dir=f"./results/lora_r{{RANK}}",
    num_train_epochs=EPOCHS,
    per_device_train_batch_size=BATCH_SIZE,
    evaluation_strategy="epoch",
    learning_rate=LR,
)

trainer = Trainer(model=model, args=args,
                  train_dataset=tokenized["train"],
                  eval_dataset=tokenized["validation"])
trainer.train()
"""


