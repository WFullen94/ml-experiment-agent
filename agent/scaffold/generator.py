def _generic_stub(method: str, cfg: dict) -> str:
    return f"""\
# Scaffold for method '{method}' — generated by ml-experiment-agent
# No template available for this method; fill in training logic below.
config = {cfg!r}

def train(config):
    # TODO: implement training for '{method}'
    raise NotImplementedError(f"training for {{config['method']!r}} not implemented")

train(config)
"""
